package de.gishmo.gwtbootstartermvp4g2.server.resource.generator.impl.gxt;

import com.google.gwt.user.client.ui.Widget;
import com.squareup.javapoet.ParameterSpec;
import de.gishmo.gwt.gwtbootstartermvp4g2.shared.model.Mvp4g2GeneraterParms;
import de.gishmo.gwtbootstartermvp4g2.server.resource.generator.impl.AbstractEventBusSourceGenerator;

import java.io.File;

public class EventBusGxtSourceGenerator
    extends AbstractEventBusSourceGenerator {

  private EventBusGxtSourceGenerator(Builder builder) {
    super();

    this.mvp4g2GeneraterParms = builder.mvp4g2GeneraterParms;
    this.directoryJava = builder.directoryJava;
    this.clientPackageJavaConform = builder.clientPackageJavaConform;
  }

  public static Builder builder() {
    return new Builder();
  }

  @Override
  protected ParameterSpec getWidgetParameterSpec() {
    return ParameterSpec.builder(Widget.class,
                                 "widget")
                        .build();
  }

  public static class Builder {

    Mvp4g2GeneraterParms mvp4g2GeneraterParms;

    File directoryJava;

    String clientPackageJavaConform;

    public Builder mvp4g2GeneraterParms(Mvp4g2GeneraterParms mvp4g2GeneraterParms) {
      this.mvp4g2GeneraterParms = mvp4g2GeneraterParms;
      return this;
    }

    public Builder directoryJava(File directoryJava) {
      this.directoryJava = directoryJava;
      return this;
    }

    public Builder clientPackageJavaConform(String clientPackageJavaConform) {
      this.clientPackageJavaConform = clientPackageJavaConform;
      return this;
    }

    public EventBusGxtSourceGenerator build() {
      return new EventBusGxtSourceGenerator(this);
    }
  }
}
